def _md_to_simple_html(text: str) -> str:
    """Very light Markdown → HTML conversion (headers, bold, newlines).

    We avoid adding a full markdown library just for this.
    """
    import re

    lines: list[str] = []
    for line in text.splitlines():
        # H3 / H2 / H1
        if line.startswith("### "):
            lines.append(f"<h3>{line[4:]}</h3>")
        elif line.startswith("## "):
            lines.append(f"<h2>{line[3:]}</h2>")
        elif line.startswith("# "):
            lines.append(f"<h1>{line[2:]}</h1>")
        else:
            # Bold **text**
            line = re.sub(r"\*\*(.+?)\*\*", r"<strong>\1</strong>", line)
            lines.append(f"<p>{line}</p>" if line.strip() else "<br>")
    return "\n".join(lines)


def _build_html(greeting: str, query: str, report: str) -> str:
    report_html = _md_to_simple_html(report)
    return f"""<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <style>
    body {{ font-family: system-ui, sans-serif; color: #1a1a1a; max-width: 800px; margin: 0 auto; padding: 24px; }}
    h1, h2, h3 {{ color: #111; }}
    .query {{ background: #f4f4f5; border-left: 4px solid #6366f1; padding: 12px 16px; border-radius: 4px; margin: 16px 0; }}
    .footer {{ font-size: 12px; color: #71717a; margin-top: 40px; border-top: 1px solid #e4e4e7; padding-top: 16px; }}
  </style>
</head>
<body>
  <p>{greeting}</p>
  <p>Your Deep Researcher report is ready.</p>
  <div class="query"><strong>Research query:</strong> {query}</div>
  <hr>
  {report_html}
  <div class="footer">Generated by <strong>Deep Researcher</strong></div>
</body>
</html>"""


