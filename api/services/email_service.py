"""Email delivery service using Resend."""

from __future__ import annotations

import logging
import textwrap

logger = logging.getLogger(__name__)


def send_report_email(
    *,
    to_email: str,
    user_name: str | None,
    query: str,
    final_report: str,
) -> bool:
    """Send the final research report by email via Resend.

    Returns True if the email was accepted, False on any error.
    """
    from api.config import settings  # local import avoids circular deps

    if not settings.resend_api_key:
        logger.warning("RESEND_API_KEY not set — skipping email delivery")
        return False

    try:
        import resend  # type: ignore[import-untyped]
    except ImportError:
        logger.error("resend package not installed — run `uv sync`")
        return False

    resend.api_key = settings.resend_api_key

    greeting = f"Hi {user_name}," if user_name else "Hi,"
    subject = f"Your research report: {textwrap.shorten(query, width=60, placeholder='…')}"

    html_body = _build_html(greeting, query, final_report)
    text_body = _build_text(greeting, query, final_report)

    try:
        resend.Emails.send(
            {
                "from": settings.email_from,
                "to": to_email,
                "subject": subject,
                "html": html_body,
                "text": text_body,
            }
        )
        logger.info("Report email sent to %s", to_email)
        return True
    except Exception:
        logger.exception("Failed to send report email to %s", to_email)
        return False


